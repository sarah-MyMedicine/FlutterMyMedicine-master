  static String translate(String key, String language) {
    return _translations[language]?[key] ?? _translations['ar']?[key] ?? key;
  }
  
  static String tr(BuildContext context, String key) {
    try {
      final settingsProvider = Provider.of<SettingsProvider>(context, listen: false);
      return translate(key, settingsProvider.language);
    } catch (e) {
      return translate(key, 'ar');
    }
  }
  
  // Simple getter without context for use in widgets with Consumer
  static Map<String, String> getTranslations(String language) {
    return _translations[language] ?? _translations['ar']!;
  }
